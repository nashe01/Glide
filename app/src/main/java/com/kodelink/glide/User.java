package com.kodelink.glide;

public class User {
    public String name;
    public String phone;
    public String email;
    public String role;

    public User() {
    }

    public User(String name, String phone, String email, String role) {
        this.name = name;
        this.phone = phone;
        this.email = email;
        this.role = role;
    }
}






